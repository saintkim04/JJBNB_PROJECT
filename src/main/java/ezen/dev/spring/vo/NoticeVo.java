package ezen.dev.spring.vo;

import java.sql.Date;

public class NoticeVo {
	
	private int n_idx;
	private String n_title;
	private String n_content;
	private Date n_date;
	private Date n_mdate;
	private String n_delyn;
	private int m_idx;
	
	
	
	
	
	public int getN_idx() {
		return n_idx;
	}
	public void setN_idx(int n_idx) {
		this.n_idx = n_idx;
	}
	public String getN_title() {
		return n_title;
	}
	public void setN_title(String n_title) {
		this.n_title = n_title;
	}
	public String getN_content() {
		return n_content;
	}
	public void setN_content(String n_content) {
		this.n_content = n_content;
	}
	public Date getN_date() {
		return n_date;
	}
	public void setN_date(Date n_date) {
		this.n_date = n_date;
	}
	public Date getN_mdate() {
		return n_mdate;
	}
	public void setN_mdate(Date n_mdate) {
		this.n_mdate = n_mdate;
	}
	public String getN_delyn() {
		return n_delyn;
	}
	public void setN_delyn(String n_delyn) {
		this.n_delyn = n_delyn;
	}
	public int getM_idx() {
		return m_idx;
	}
	public void setM_idx(int m_idx) {
		this.m_idx = m_idx;
	}
	
	
	
	

}
